        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Register(RegisterViewModel model)
        {
			
			if (ModelState.IsValid)
            {
               if(model.Password != model.ConfirmPassword)
				{
					ModelState.AddModelError("", "Password does not match");
					return View(model);
				}
				var hasNumber = new Regex(@"[0-9]+");
				var hasUpperChar = new Regex(@"[A-Z]+");
				var hasMiniMaxChars = new Regex(@".{8,15}");
				var hasLowerChar = new Regex(@"[a-z]+");
				var hasSymbols = new Regex(@"[!@#$%^&*()_+=\[{\]};:<>|./?,-]");
				var input = model.Password;
				if (!hasLowerChar.IsMatch(input))
				{
					ModelState.AddModelError ("", "Password should contain At least one lower case letter");
					return View(model);
				}
				else if (!hasUpperChar.IsMatch(input))
				{
					ModelState.AddModelError("","Password should contain At least one upper case letter");
					return View(model); 
				}
				else if (!hasMiniMaxChars.IsMatch(input))
				{
					ModelState.AddModelError("","Password should not be less than 8 or greater than 12 characters");
					return View(model);
				}
				else if (!hasNumber.IsMatch(input))
				{
					ModelState.AddModelError ("", "Password should contain At least one numeric value");
					return View(model);
				}

				
				
				Account account = new Account();
				account.Email = model.Email;
				account.Password = model.Password;
				account.Status = true;
				Account_Dao dao = new Account_Dao();
				var user = dao.GetById(model.Email);
				var userSession = new UserLogin();
				userSession.Email = user.Email;
				userSession.UserId = user.Id;

				Session.Add(CommonConstaint.USER_SESSION, userSession);
				return RedirectToAction("Index", "Home");
               
            }
			
			// If we got this far, something failed, redisplay form
			return View(model);
        }

